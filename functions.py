from math import pi

def area_of_circle(r): #this is the way to define a function, include the variables in the parenthesis
    area = pi * r**2
    print(area)

area_of_circle(1) #this is the way to call out or use a function

def area_of_triangle(b,h):
    area = 1/2 * b * h
    print(area)

area_of_triangle(1,2)
area_of_triangle(12,56)





